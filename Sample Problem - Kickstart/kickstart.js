var test_case = readline();
var k=1;

function main(){
  while(test_case){
    var n, m, c;
    n = readline();
    m = readline();
  
    for(var i = 0; i < n; i++){
      c.push(readline())
    }
  
    var sum = 0;
  
    for(var i = 0; i < n; i++){
      sum = c[i];
    }
  
    var ans = sum%m;
  
    print(`Case #${k}: ${ans} \n`);

    test_case--;
  }
}